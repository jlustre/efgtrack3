    <p>The Associate understands and acknowledges that:</p>
    <ul class="list-disc pl-5">
        <li>Participation in the organization does not create an employment relationship.</li>
        <li>The Associate operates as an independent contractor where permitted by law and company policy.</li>
        <li>The Associate is responsible for managing their own schedule, activities, and business efforts.</li>
        <li>The Associate is responsible for complying with all applicable tax obligations and regulatory requirements.</li>
        <li>No guarantee of income, commissions, bonuses, promotions, or business success is expressed or implied.</li>
    </ul>

    <h2 class="mt-8 text-lg font-semibold text-[#0B1F3A]">Professional Conduct</h2>
    <p>The Associate agrees to conduct business honestly and ethically, treat clients and team members with respect, follow applicable laws and company policies, avoid misleading statements, maintain confidentiality, and protect the reputation of the organization.</p>

    <h2 class="mt-8 text-lg font-semibold text-[#0B1F3A]">Compliance Responsibilities</h2>
    <p>The Associate agrees to complete required compliance training, AML training where required, licensing requirements, maintain licenses and certifications, provide accurate information, and follow advertising and marketing guidelines.</p>

    <h2 class="mt-8 text-lg font-semibold text-[#0B1F3A]">Training &amp; Development</h2>
    <p>The Associate agrees to actively participate in onboarding activities, required training programs, Field Apprenticeship Program activities, mentorship meetings, continuing education, and skill development opportunities.</p>

    <h2 class="mt-8 text-lg font-semibold text-[#0B1F3A]">Certified Field Mentor Relationship</h2>
    <p>The Associate understands that a Certified Field Mentor may be assigned during onboarding to provide guidance and support, and agrees to maintain regular communication with their assigned mentor.</p>

    <h2 class="mt-8 text-lg font-semibold text-[#0B1F3A]">Portal Usage Agreement</h2>
    <p>The Associate agrees to use the EFGTrack platform responsibly, maintain accurate profile information, protect login credentials, and use the portal only for authorized business purposes.</p>

    <h2 class="mt-8 text-lg font-semibold text-[#0B1F3A]">Confidential Information</h2>
    <p>The Associate acknowledges access to confidential information and agrees not to share, reproduce, or use such information without authorization. This obligation survives termination.</p>

    <h2 class="mt-8 text-lg font-semibold text-[#0B1F3A]">No Earnings Guarantee</h2>
    <p>Individual results vary. Earnings depend on effort, skill, experience, market conditions, and other factors. The Associate accepts full responsibility for their own business activities and results.</p>

