<h2>Purpose of This Agreement</h2>
<p>This Associate Participation Agreement outlines the expectations, responsibilities, and standards applicable to associates participating in the organization's training, onboarding, mentorship, licensing, and business development activities.</p>

<h2>Independent Status</h2>
<ul>
    <li>Participation in the organization does not create an employment relationship.</li>
    <li>The Associate operates as an independent contractor where permitted by law and company policy.</li>
    <li>The Associate is responsible for managing their own schedule, activities, and business efforts.</li>
    <li>The Associate is responsible for complying with all applicable tax obligations and regulatory requirements.</li>
    <li>No guarantee of income, commissions, bonuses, promotions, or business success is expressed or implied.</li>
</ul>

<h2>Professional Conduct</h2>
<ul>
    <li>Conduct business honestly and ethically.</li>
    <li>Treat clients, prospects, team members, and leaders with professionalism and respect.</li>
    <li>Follow all applicable laws, regulations, and company policies.</li>
    <li>Avoid misleading statements or misrepresentations.</li>
    <li>Maintain confidentiality of sensitive information.</li>
    <li>Protect the reputation of the organization and its partners.</li>
</ul>

<h2>Compliance Responsibilities</h2>
<ul>
    <li>Complete all required compliance training.</li>
    <li>Complete Anti-Money Laundering (AML) training where required.</li>
    <li>Complete licensing requirements applicable to their jurisdiction.</li>
    <li>Maintain all required licenses and certifications.</li>
    <li>Provide accurate and truthful information during onboarding and business activities.</li>
    <li>Follow all advertising, marketing, and solicitation guidelines.</li>
</ul>

<h2>Training &amp; Development</h2>
<ul>
    <li>Onboarding activities</li>
    <li>Required training programs</li>
    <li>Field Apprenticeship Program (FAP) activities</li>
    <li>Mentorship meetings</li>
    <li>Continuing education programs</li>
    <li>Skill development opportunities</li>
</ul>

<h2>Certified Field Mentor Relationship</h2>
<ul>
    <li>A Certified Field Mentor (CFM) may be assigned during onboarding.</li>
    <li>The mentor's role is to provide guidance, accountability, and support.</li>
    <li>The mentor relationship does not create any guarantee of results or success.</li>
    <li>Active participation and communication are required for mentorship to be effective.</li>
</ul>

<h2>Portal Usage Agreement</h2>
<ul>
    <li>Maintain accurate profile information.</li>
    <li>Protect login credentials and passwords.</li>
    <li>Keep information secure and confidential.</li>
    <li>Use the portal only for authorized business purposes.</li>
    <li>Refrain from unauthorized access to information belonging to other users.</li>
</ul>

<h2>Confidential Information</h2>
<ul>
    <li>Share confidential information without authorization.</li>
    <li>Reproduce proprietary materials without permission.</li>
    <li>Use confidential information for unauthorized purposes.</li>
</ul>

<h2>Prospect &amp; Client Information</h2>
<ul>
    <li>Handle prospect information responsibly.</li>
    <li>Maintain confidentiality of client information.</li>
    <li>Comply with privacy laws and regulations.</li>
    <li>Use information only for authorized business purposes.</li>
</ul>

<h2>Electronic Communications</h2>
<ul>
    <li>System notifications</li>
    <li>Training updates</li>
    <li>Onboarding reminders</li>
    <li>Mentorship communications</li>
    <li>Licensing notifications</li>
    <li>Compliance updates</li>
</ul>

<h2>Intellectual Property</h2>
<ul>
    <li>Copy proprietary materials for unauthorized use.</li>
    <li>Sell or distribute protected content.</li>
    <li>Remove copyright or ownership notices.</li>
</ul>

<h2>No Earnings Guarantee</h2>
<div class="note">Individual results vary. Earnings depend on effort, skill, experience, market conditions, and numerous other factors. No representation is made regarding specific earnings or business outcomes.</div>

<h2>Code of Ethics</h2>
<ul>
    <li>Act with integrity.</li>
    <li>Serve clients honestly.</li>
    <li>Place client interests first.</li>
    <li>Follow ethical business practices.</li>
    <li>Respect regulatory requirements.</li>
    <li>Maintain professionalism at all times.</li>
</ul>

<h2>Termination</h2>
<ul>
